class imageGetter:
    image = None
    cx = None
    cy = None

    def getImage(self):
        return self.image

    def setImage(self, image1):
        self.image = image1
    
    def getCX(self):
        return self.cx
    
    def getCY(self):
        return self.cy

    def setCX(self, cx1):
        self.cx = cx1

    def setCy(self, cy1):
        self.cy = cy1
    